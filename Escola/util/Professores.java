package Escola.util;

public class Professores {
    
    private String nome;
    private String sexo;
    private String datadeNascimento;
    private String cpf;
    private String numerodeInscricao;

    public Professores(String nome, String sexo, String datadeNascimento, String cpf, String numerodeInscricao) {
        this.nome = nome;
        this.sexo = sexo;
        this.datadeNascimento = datadeNascimento;
        this.cpf = cpf;
        this.numerodeInscricao = numerodeInscricao;
    }
    
}
