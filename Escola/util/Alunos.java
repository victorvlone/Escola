package Escola.util;

public class Alunos {

    private String nome;
    private String sexo;
    private String datadeNascimento;
    private String cpf;
    private String numerodeMatricula;

    public Alunos() {
    }

    public Alunos(String nome, String sexo, String datadeNascimento, String cpf, String numerodeMatricula) {

            this.nome = nome;
            this.sexo = sexo;
            this.datadeNascimento = datadeNascimento;
            this.cpf = cpf;
            this.numerodeMatricula = numerodeMatricula;

    }
}
